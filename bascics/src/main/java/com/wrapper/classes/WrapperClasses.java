package com.wrapper.classes;

public class WrapperClasses {

	private Integer i = 10;
	private Float j = 1.23f;
	private Double k = 12.22;
	private Character l = 'c';

	public static void main(String[] args) {
		WrapperClasses wrapperClasses = new WrapperClasses();
		System.out.println(wrapperClasses.i);
		System.out.println(wrapperClasses.j);
		System.out.println(wrapperClasses.k);
		System.out.println(wrapperClasses.l);
	}

}
